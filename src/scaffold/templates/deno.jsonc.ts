import type { TemplateContext } from "@scaffold/types";

const DEFAULT_VERSION = "0.0.1-rc.0";

export const metadata = { outputPath: "deno.jsonc" };

export default function (context: TemplateContext): string {
  const tasks = context.includeConfig
    ? `  "tasks": {
    "test": "deno test --allow-read --coverage=coverage ./tests",
    "bench": "deno bench ./benchmarks",
    "fmt": "deno fmt",
    "lint": "deno lint ./src ./tests ./benchmarks ./examples",
    "check": "deno check ./src/mod.ts",
    "ci": "deno task fmt && deno task lint --fix && deno task check && deno task test"
  },`
    : `  "tasks": {
    "fmt": "deno fmt",
    "lint": "deno lint ./src ./examples",
    "check": "deno check ./src/mod.ts",
    "ci": "deno task fmt && deno task lint --fix && deno task check"
  },`;

  return `{
  // Generated by deno-app-scaffold-cli on ${context.generatedAtIso}
  "name": "${context.scope}/${context.packageName}",
  "version": "${DEFAULT_VERSION}",
  "fmt": {
    "lineWidth": 88,
    "indentWidth": 2,
    "useTabs": false,
    "semiColons": true,
    "singleQuote": false,
    "proseWrap": "preserve",
    "trailingCommas": "onlyMultiLine",
    "operatorPosition": "nextLine"
  },
  "lint": {
    "rules": {
      "tags": ["recommended"]
    }
  },
  "compilerOptions": {
    "strict": true
  },
  "exports": {
    ".": "./src/mod.ts"
  },
${tasks}
  "exclude": [".*", "coverage"]
}
`;
}
